package fr.bludwarf.commons.xml.exceptions;

public class XMLLoadException extends Exception
{

	/**
	 * 
	 */
	private static final long serialVersionUID = 4745333603725836905L;

	public XMLLoadException()
	{
	}

	public XMLLoadException(String arg0)
	{
		super(arg0);
	}

	public XMLLoadException(Throwable arg0)
	{
		super(arg0);
	}

	public XMLLoadException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

}
